// Implicit conversion
let result = "5" + 3;                           // "53" (string concatenation)
console.log(result);


let result2 = "5" - 3;                          // 2 (numeric subtraction)
console.log(result2);


let result3 = "5" * "2";                        // 10 (numeric multiplication)
console.log(result3);




// Explicit conversion
let str = String(123);                      // "123"
let num = Number("456");                    // 456
let bool = Boolean(1);                      // true



