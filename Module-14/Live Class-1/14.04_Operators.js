
let a = 10, b = 3;

console.log(a + b);                    // 13 (Addition)
console.log(a - b);                    // 7 (Subtraction)
console.log(a * b);                    // 30 (Multiplication)
console.log(a / b);                    // 3.333 ... (Division)
console.log(a % b);                     // 1 (Modulus/Remainder)
console.log(a ** b);                   // 1000 (Exponentiation)




// Increment/Decrement

let x = 5;

x++;            // x = 6 (post-increment)
++x;            // x = 7 (pre-increment)
x--;            // x = 6 (post-decrement)
--x;            // x = 5 (pre-decrement)






// Comparison Operators

let p = 5, q = "5";

console.log(p == q);                // true--> only checks the Value (loose equality)
console.log(p === q);               // false--> checks both Value & Type (strict equality)
console.log(p != q);
console.log(p !== q);







// Logical Operators

let y = true, z = false;

console.log(y && z);                  // false (AND)
console.log(y || z);                  // true (OR)
console.log(!y);                     // false (NOT)





